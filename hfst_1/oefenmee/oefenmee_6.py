# Start de oefen mee met onderstaande dictionary.
filmscores = {
    "godfather": 9,
    "avatar": 3,
    "oppenheimer": 7.5
}
for film in filmscores: # voor elke key(film) in de dictionarie 
    waarde= filmscores[film] # haal de waarde(score) van de film uit de dictionarie
    print(f'de film {film} kreeg scorde van {waarde} op 10')


    